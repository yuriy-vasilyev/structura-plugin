/**
 * Resolve a provider id (`gemini` | `openai` | `anthropic`) to its brand
 * logo component. Returns `null` for an unknown/absent provider so callers
 * can fall back to a neutral swatch. Accepts a couple of aliases the catalog
 * and wire payloads use interchangeably (`google` → Gemini, `claude` →
 * Claude).
 */
export function getProviderLogo(
  provider: string | null | undefined,
): FC<ProviderLogoProps> | null {
  switch ((provider ?? "").toLowerCase()) {
    case "gemini":
    case "google":
      return GeminiLogo;
    case "openai":
      return OpenAILogo;
    case "anthropic":
    case "claude":
      return ClaudeLogo;
    default:
      return null;
  }
}
